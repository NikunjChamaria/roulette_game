import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

TextStyle lobster(Color color, double size, FontWeight fw) {
  return GoogleFonts.lobster(
    color: color,
    fontSize: size,
    fontWeight: fw,
  );
}

TextStyle spacegrotesk(Color color, double size, FontWeight fw) {
  return GoogleFonts.robotoCondensed(
    color: color,
    fontSize: size,
    fontWeight: fw,
  );
}
